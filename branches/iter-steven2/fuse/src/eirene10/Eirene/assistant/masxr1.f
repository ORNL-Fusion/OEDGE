C*DK MASXR1
      SUBROUTINE MASXR1 (A,B,KI,KE,KST,TX)
      USE PRECISION
      USE COMPRT, ONLY: IUNOUT
      IMPLICIT NONE
      CHARACTER(8), INTENT(IN) :: TX(*)
      CHARACTER(14), INTENT(IN) :: A
      REAL(DP), INTENT(IN) :: B(*)
      INTEGER, INTENT(IN) :: KI, KE, KST
      INTEGER :: K
      IF (KE.LT.KI) RETURN
      WRITE (iunout,60) A
60    FORMAT (1X,A14)
         WRITE (iunout,61) (TX(K),B(K),K=KI,KE,KST)
61    FORMAT (1X,6(A8,1PE12.4,1X))
      RETURN
      END
