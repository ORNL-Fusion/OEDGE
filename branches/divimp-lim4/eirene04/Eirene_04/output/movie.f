C
      SUBROUTINE MOVIE
C
C  MODIFICATIONS TO INPUT, IN ORDER TO PERFORM MANY TIME STEPS
C  AND TO PRODUCE A MOVIE WITH TEST PARTICLE HISTORIES
C
      USE PRECISION
      USE PARMMOD
      USE CPLOT
      USE CTRCEI
      USE COMPRT
      USE COMNNL
      USE COMSOU

      IMPLICIT NONE

      INTEGER :: ISTR

C  TRY TO MAINTAIN A SWARM OF NPRNLI SOURCE PARTICLES AT EACH TIMESTEP,
C  COLD START FROM CENSUS

      PLHST=.TRUE.
      I1TRC=1
      I2TRC=NPRNLI
C
      NVOLPR=0
      NVOLPL=0
C
      DO ISTR=1,NSTRAI
        NINITL(ISTR)=0
      ENDDO
C
      RETURN
      END
