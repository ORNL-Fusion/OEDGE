

C*DK MASAJR
      SUBROUTINE MASAJR (A,B,N,R)
      USE PRECISION
      USE COMPRT, ONLY: IUNOUT
      IMPLICIT NONE
      CHARACTER(24), INTENT(IN) :: A
      CHARACTER(8), INTENT(IN) :: B
      INTEGER, INTENT(IN) :: N
      REAL(DP), INTENT(IN) :: R
      WRITE (iunout,60) A,B,N,R
60    FORMAT (1X,A24,1X,A8,1X,I2,1X,1PE12.4)
      RETURN
      END
