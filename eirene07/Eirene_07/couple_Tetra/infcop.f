      NAINI=NAINB
C
C
      NLPLG=.FALSE.
      NLTET=.TRUE.
      LEVGEO=5
      NR1ST=NTET+1
      NLPOL=.FALSE.
      NP2ND=1
      NLTOR=.FALSE.
      NT3RD=1
CPB   NR1TAL=NCLTAL(NTET)+NRADD
CPB   NP2TAL=NP2ND
CPB   NT3TAL=NT3RD
      NR1TAL=NDYA
      NP2TAL=NDXA
      NT3TAL=NDZA

      DO IN=NTET+1,NR1ST+NRADD
        NCLTAL(IN)=NCLTAL(NTET)+IN-NTET
