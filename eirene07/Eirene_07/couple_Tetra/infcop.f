        DISTEP(1:NPLSI,ITARG,IG)=DIINTF(1:NPLSI,IN)
        VXSTEP(1:NPLSV,ITARG,IG)=VXINTF(1:NPLSV,IN)
        VYSTEP(1:NPLSV,ITARG,IG)=VYINTF(1:NPLSV,IN)
        VZSTEP(1:NPLSV,ITARG,IG)=VZINTF(1:NPLSV,IN)
