                NTOLD=((IT-2)*NDXYM+(IP-1)*NDYAM+IR-2)*6

                IF (NTBAR(1,NTACT+2) == 0) THEN
                  NTBAR(1,NTACT+2)=NTOLD+6
                  NTSEITE(1,NTACT+2)=3
                  NTBAR(3,NTOLD+6)=NTACT+2
