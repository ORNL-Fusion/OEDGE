                NTOLD=((IT-3)*NDXYM+(IP-1)*NDYAM+IR-1)*6

                IF (NTBAR(1,NTACT+1) == 0) THEN
                  NTBAR(1,NTACT+1)=NTOLD+3
                  NTSEITE(1,NTACT+1)=3
                  NTBAR(3,NTOLD+3)=NTACT+1
                  NTSEITE(3,NTOLD+3)=1
