        WRITE (6,*)
C       DO I28=1,28
C         DO I11=1,11
C           WRITE (6,*) NTARG(I28),NPROJ(I11)
C           WRITE (6,*) 'ES   ',ES(I28)
C           WRITE (6,*) 'M2M1 ',M2M1(I28,I11)
C           WRITE (6,*) 'ETF  ',ETF(I28,I11)
C           WRITE (6,*) 'ETH  ',ETH(I28,I11)
C           WRITE (6,*) 'Q    ',Q(I28,I11)
C           WRITE (6,*)
C         ENDDO
C       ENDDO
      ENDIF
