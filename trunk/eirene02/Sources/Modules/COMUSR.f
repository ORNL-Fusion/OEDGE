      SUBROUTINE ALLOC_COMUSR (ICAL)

      INTEGER, INTENT(IN) :: ICAL

      IF (ICAL == 1) THEN

        IF (ALLOCATED(TEIN)) RETURN

        NSFPRM=2*NLMPGS
        NPLPR2=(2+4*NPLS)*NRAD+3*(NATM+NMOL+NION+NPLS)+4+NSPZ
        NUSR=NPLPRM+NPLPR2+NSFPRM
        MUSR=4*NATM+4*NMOL+5*NION+3*NPLS+25+NSPZ+
     .       6*(1+NPHOTP)*(1+NATMP)*(1+NMOLP)*(1+NIONP)*(1+NPLSP)+NSPZ*6
        LUSR=NRAD*(NPLS+2)+NRAD
C
        ALLOCATE (TEIN(NRAD))
        ALLOCATE (TIIN(NPLS,NRAD))
        ALLOCATE (DEIN(NRAD))
        ALLOCATE (DIIN(NPLS,NRAD))
        ALLOCATE (VXIN(NPLS,NRAD))
        ALLOCATE (VYIN(NPLS,NRAD))
        ALLOCATE (VZIN(NPLS,NRAD))
        ALLOCATE (BXIN(NRAD))
        ALLOCATE (BYIN(NRAD))
        ALLOCATE (BZIN(NRAD))
        ALLOCATE (BFIN(NRAD))
        ALLOCATE (VOL(NRAD))
        ALLOCATE (WGHT(NSPZMC,NRAD))
        ALLOCATE (FLXOUT(NLMPGS))
        ALLOCATE (SAREA(NLMPGS))
        ALLOCATE (TEINL(NRAD))
        ALLOCATE (TIINL(NPLS,NRAD))
        ALLOCATE (BVIN(NPLS,NRAD))
        ALLOCATE (PARMOM(NPLS,NRAD))
        ALLOCATE (EDRIFT(NPLS,NRAD))
        ALLOCATE (DEINL(NRAD))
        ALLOCATE (DIINL(NPLS,NRAD))
        ALLOCATE (RMASSI(NION))
        ALLOCATE (RMASSA(NATM))
        ALLOCATE (RMASSM(NMOL))
        ALLOCATE (RMASSP(NPLS))
        ALLOCATE (DIOD(NION))
        ALLOCATE (DATD(NATM))
        ALLOCATE (DMLD(NMOL))
        ALLOCATE (DPLD(NPLS))
        ALLOCATE (DION(NION))
        ALLOCATE (DATM(NATM))
        ALLOCATE (DMOL(NMOL))
        ALLOCATE (DPLS(NPLS))
        ALLOCATE (TEXTS(NSPZ))
        ALLOCATE (NMASSA(NATM))
        ALLOCATE (NCHARA(NATM))
        ALLOCATE (NFOLA(NATM))
        ALLOCATE (NGENA(NATM))
        ALLOCATE (NMASSM(NMOL))
        ALLOCATE (NCHARM(NMOL))
        ALLOCATE (NFOLM(NMOL))
        ALLOCATE (NGENM(NMOL))
        ALLOCATE (NMASSP(NPLS))
        ALLOCATE (NCHARP(NPLS))
        ALLOCATE (NCHRGP(NPLS))
        ALLOCATE (NMASSI(NION))
        ALLOCATE (NCHARI(NION))
        ALLOCATE (NCHRGI(NION))
        ALLOCATE (NFOLI(NION))
        ALLOCATE (NGENI(NION))
        ALLOCATE (NFOLPH(NPHOT))
        ALLOCATE (NGENPH(NPHOT))
        ALLOCATE (NPRT(NSPZ))
        ALLOCATE (ISPEZ(-1:4,0:NPHOTP,0:NATMP,0:NMOLP,0:NIONP,0:NPLSP))
        ALLOCATE (ISPEZI(NSPZ,-1:4))
        ALLOCATE (IADVE(NADV))
        ALLOCATE (IADVS(NADV))
        ALLOCATE (IADVT(NADV))
        ALLOCATE (IADRC(NADV))
        ALLOCATE (ICLVE(NCLV))
        ALLOCATE (ICLVS(NCLV))
        ALLOCATE (ICLVT(NCLV))
        ALLOCATE (ICLRC(NCLV))
        ALLOCATE (ISNVE(NSNV))
        ALLOCATE (ISNVS(NSNV))
        ALLOCATE (ISNVT(NSNV))
        ALLOCATE (ISNRC(NSNV))
        ALLOCATE (IADSE(NADS))
        ALLOCATE (IADSS(NADS))
        ALLOCATE (IADST(NADS))
        ALLOCATE (IADSC(NADS))
        ALLOCATE (NFRSTP(NTALI))
        ALLOCATE (NADDP(NTALI))
        ALLOCATE (NSPAN(NTALV))
        ALLOCATE (NSPEN(NTALV))
        ALLOCATE (LGVAC(NRAD,0:NPLS+1))
        ALLOCATE (LGDFT(NRAD))

        WRITE (55,*) ' COMUSR(1) ',NUSR*8 + MUSR*4 + LUSR*4

      ELSE IF (ICAL == 2) THEN

        IF (ALLOCATED(ADIN)) RETURN

        NPLPR1=(6+5*NPLS+NAIN)*NRAD
        NPLPRM=NPLPR1+(1+1*NPLS+NSPZMC)*NRAD
        ALLOCATE (ADIN(NAIN,NRAD))
        ALLOCATE (ICPVE(NCPV))
        ALLOCATE (ICPVS(NCPV))
        ALLOCATE (ICPVT(NCPV))
        ALLOCATE (ICPRC(NCPV))
        ALLOCATE (IBGVE(NBGV))
        ALLOCATE (IBGVS(NBGV))
        ALLOCATE (IBGVT(NBGV))
        ALLOCATE (IBGRC(NBGV))

        WRITE (55,*) ' COMUSR(2) ',NAIN*NRAD*8 + 4*(NCPV+NBGV)*4

      END IF

      CALL INIT_COMUSR(ICAL)

      RETURN
      END SUBROUTINE ALLOC_COMUSR


