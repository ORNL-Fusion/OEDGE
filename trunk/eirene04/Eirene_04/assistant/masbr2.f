      SUBROUTINE MASBR2 (A,NLFLD,NFIRST0,NFIRST1,N0,N1,M,NBITS)
      IMPLICIT NONE
      CHARACTER(*), INTENT(IN) :: A
      INTEGER, INTENT(IN) :: NFIRST0, NFIRST1, N0, N1, M, NBITS
      INTEGER, INTENT(IN) :: NLFLD(NFIRST0:NFIRST1,*)
      INTEGER :: I, J, K, JA, IZ, JE, NCH
      LOGICAL BITGET
C
      NCH=110
      IZ=M/NCH
      IF (MOD(M,NCH).NE.0) IZ=IZ+1
C
      WRITE (6,'(///1X,A)') A
      DO 1 I=1,IZ
         JA=(I-1)*NCH+1
         JE=MIN(I*NCH,M)
         WRITE (6,'(/6X,A1,12(1X,10I1))') 'I',(MOD(J,10),J=JA,JE)
         WRITE (6,'(1X,130A1)') ('-',J=JA,JE+16)
         DO 2 K=N0,N1
            WRITE (6,'(1X,I4,1X,A1,12(1X,10L1))') K,'I',
     .       (BITGET(NLFLD,NFIRST0,NFIRST1,K,J,NBITS),J=JA,JE)
2        CONTINUE
1     CONTINUE
      RETURN
      END
