C*DK DETER
      FUNCTION DETER(A11,A21,A31,A12,A22,A32,A13,A23,A33)
C
C  DETERMINAT OF (3,3)-MATRIX A
C
      USE PRECISION
      IMPLICIT NONE
      REAL(DP), INTENT(IN) :: A11, A21, A31, A12, A22, A32, A13, A23, 
     .                        A33
      REAL(DP) :: S, DETER

      S=A11*(A22*A33-A23*A32)
      S=S-A12*(A21*A33-A23*A31)
      S=S+A13*(A21*A32-A22*A31)
      DETER=S
      RETURN
      END
