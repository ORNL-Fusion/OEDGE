      SUBROUTINE XSTAPI_PARAM

      USE PRECISION
      USE PARMMOD
      USE COMUSR
      USE CCONA
      USE CLOGAU
      USE CGRID
      USE CZT1
      USE CTRCEI
      USE COMPRT
      USE COMXS
      USE CSPEI

      IMPLICIT NONE

      INTEGER :: KK, NRC
C
C   ION IMPACT COLLISIONS
C
      DO 1000 IATM=1,NATMI
C
C  NO DEFAULT MODEL
C
       IF (NRCA(IATM).EQ.0) THEN
C
C  NON DEFAULT ION IMPACT MODEL:  130--190
C
        ELSEIF (NRCA(IATM).GT.0) THEN
          DO 130 NRC=1,NRCA(IATM)
            KK=IREACA(IATM,NRC)
            IF (ISWR(KK).NE.4) GOTO 130
            NRPI=NRPI+1
C
130       CONTINUE
C
C  NO MODEL DEFINED
        ELSE
        ENDIF
C
1000  CONTINUE
C
      RETURN
C
      END
