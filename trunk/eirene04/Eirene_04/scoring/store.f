      SUBROUTINE STORE(IFLAG)

C  PURPOSE: STORE TRAJECTORIES
C  TO BE WRITTEN

      IMPLICIT NONE

      INTEGER :: IFIRST, IFLAG
      DATA IFIRST/0/

      IF (IFIRST.EQ.0) THEN
        IFIRST=1
        OPEN (UNIT=16,ACCESS='SEQUENTIAL',FORM='UNFORMATTED')
      ENDIF
      GOTO (10,20,30,40,50,60,70,80,90),IFLAG
      WRITE (6,*) 'IFLAG OUT OF RANGE IN SUBR. STORE '
      WRITE (6,*) 'EXIT CALLED '
      CALL EXIT_OWN(1)
C  LOCATE
10    CONTINUE
20    CONTINUE
C  IONIZATION
30    CONTINUE
40    CONTINUE
50    CONTINUE
C  SURFACE
60    CONTINUE
70    CONTINUE
80    CONTINUE
C  NEW CELL
90    CONTINUE
      RETURN
      END
