         INC=FAI*SQRT(V)
         IF (PH2-PH1.GT.ZWOPI) THEN
            WI1=0.
            WI2=ZWOPI
         ELSE
            WI1=PH1
            WI2=PH2
            IF (WI1.LT.-ZWOPI) WI1=-ZWOPI
            IF (WI2.GT. ZWOPI) WI2= ZWOPI
         ENDIF
         IF (V.GT.GREAT) THEN
            I1=(WI1+ZWOPI)*ZPIFAC+.5
            I2=(WI2+ZWOPI)*ZPIFAC+.5
            IF (I1.NE.I2) THEN
               K=1
               DO 1 I=I1,I2,INC
                  X(K)=FAX*CP(I)+XM
                  Y(K)=FAY*SP(I)+YM
                  K=K+1
    1          CONTINUE
               IF (I.NE.I2+INC) THEN
                  X(K)=FAX*CP(I2)+XM
                  Y(K)=FAY*SP(I2)+YM
                  K=K+1
               ENDIF
            ELSE
               K=2
            ENDIF
         ELSE
            CH1=(WI1+ZWOPI)*ZPIFAC
            I1=CH1
            COCH=CP(I1)+(CP(I1+1)-CP(I1))*(CH1-I1)
            X(1)=FAX*COCH+XM
            SICH=SP(I1)+(SP(I1+1)-SP(I1))*(CH1-I1)
            Y(1)=FAY*SICH+YM
            CH2=(WI2+ZWOPI)*ZPIFAC
            I2=CH2
            K=2
            DO 2 I=I1,I2,INC
               X(K)=FAX*CP(I)+XM
               Y(K)=FAY*SP(I)+YM
               K=K+1
    2       CONTINUE
            COCH=CP(I2)+(CP(I2+1)-CP(I2))*(CH2-I2)
            X(K)=FAX*COCH+XM
            SICH=SP(I2)+(SP(I2+1)-SP(I2))*(CH2-I2)
            Y(K)=FAY*SICH+YM
            K=K+1
         ENDIF
         IF (K.GE.3) THEN
            IF (K-1.GT.MAXPKT) THEN
               CALL GPL(MAXPKT,X(1),y(1))
               CALL GPL(K-MAXPKT,X(MAXPKT),y(MAXPKT))
            ELSE
               CALL GPL(K-1,X,Y)
            ENDIF
         ENDIF
      ENDIF
      END
