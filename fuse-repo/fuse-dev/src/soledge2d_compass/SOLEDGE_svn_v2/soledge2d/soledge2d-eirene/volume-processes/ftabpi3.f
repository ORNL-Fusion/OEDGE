 
 
      FUNCTION EIRENE_FTABPI3 (IRPI,K)
 
      USE EIRMOD_PRECISION
      USE EIRMOD_PARMMOD
      USE EIRMOD_COMUSR
      USE EIRMOD_COMPRT
      USE EIRMOD_COMXS
 
      IMPLICIT NONE
 
      INTEGER, INTENT(IN) :: IRPI, K
      REAL(DP) :: TBPIC(9), EIRENE_FTABPI3, PLS, TBPI, 
     .            EIRENE_RATE_COEFF, ERATE
      INTEGER :: J, I, II, KK, IPLSTI
 
      TBPI=0.D0
      KK = NREAPI(IRPI)
 
      IPLSTI=MPLSTI(IPLS)
      PLS=TIINL(IPLSTI,K)+ADDPI(IRPI,IPLS)
 
      TBPI = EIRENE_RATE_COEFF(KK,PLS,0._DP,.TRUE.,0,ERATE)*FACREA(KK,1)
      TBPI = TBPI*DIIN(IPLS,K)
 
      EIRENE_FTABPI3 = TBPI
 
      RETURN
      END
 
