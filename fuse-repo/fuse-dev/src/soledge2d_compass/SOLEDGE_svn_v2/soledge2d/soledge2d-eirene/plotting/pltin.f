      SUBROUTINE EIRENE_PLTIN (RIB,FCN,ANF,END,INN,LBOX)
 
      USE EIRMOD_PRECISION
      USE EIRMOD_PARMMOD
      USE EIRMOD_CLMSUR
      USE EIRMOD_CTRCEI
 
      IMPLICIT NONE
      REAL(DP), INTENT(IN) :: RIB, ANF, END
      REAL(DP) :: FCN
      INTEGER, INTENT(OUT) :: INN
      LOGICAL :: LBOX
      EXTERNAL FCN
 
      IF (RIB.EQ.1..OR.RIB.EQ.0.)
     .                CALL EIRENE_PLTKI (FCN,ANF,END,INN,TRCPLT,LBOX)
      IF (RIB.EQ.1.5) CALL EIRENE_PLTKA (FCN,ANF,END,INN,TRCPLT,LBOX)
      IF (RIB.LT.0.)  CALL EIRENE_PLTKU (FCN,ANF,END,INN,TRCPLT,LBOX)
      RETURN
      END
